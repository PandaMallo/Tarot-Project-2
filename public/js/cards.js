const cardBack1 = document.getElementById('card-back-1');
cardBack1.addEventListener('click', () => {
  document
    .getElementById('card-back-1')
    .classList.remove('d-flex', 'flex-column');
  document.getElementById('card-back-1').classList.add('d-none');
  document.getElementById('card-show-1').classList.remove('d-none');
  document.getElementById('card-show-1').classList.add('d-flex', 'flex-column');
});

